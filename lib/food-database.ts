/** Формує текстову таблицю довідника для системного промпту ШІ */
export function buildFoodDatabasePrompt(): string {
  const byCategory = new Map<string, FoodItem[]>();
  for (const item of FOOD_DATABASE) {
    const list = byCategory.get(item.category) ?? [];
    list.push(item);
    byCategory.set(item.category, list);
  }

  const sections: string[] = [];
  for (const [category, items] of byCategory) {
    const rows = items
      .map((it) => {
        const macros = `Б ${it.protein} / Ж ${it.fat} / В ${it.carbs} / ${it.calories} ккал`;
        const fiber = it.fiber != null ? `, клітк. ${it.fiber} г` : "";
        const note = it.note ? ` — ${it.note}` : "";
        return `  • ${it.name} [${it.per}]: ${macros}${fiber}${note}`;
      })
      .join("\n");
    sections.push(`${category}:\n${rows}`);
  }

  return `ЖОРСТКИЙ ДОВІДНИК ПРОДУКТІВ (Золотий стандарт, джерело Calorizator) — БЖУ НА ВКАЗАНУ КІЛЬКІСТЬ:

${sections.join("\n\n")}`;
}

/** Готовий текст довідника для вставки в промпти */
export const FOOD_DATABASE_PROMPT = buildFoodDatabasePrompt();

